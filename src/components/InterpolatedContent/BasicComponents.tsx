import React from 'react';
export const Div = ({ children, className, style }: any) => <div className={className} style={style}>{children}</div>;
export const Span = ({ children, className, style }: any) => <span className={className} style={style}>{children}</span>;
export const Strong = ({ children, className, style }: any) => <strong className={className} style={style}>{children}</strong>;